friends = ["Kevin","Karen","jim","Oscar","Toby"]

print(friends)

print(friends[0])           # '0' = kevin

print(friends[-1])          # '-1'= jim

print(friends[-2])          # '-2'= karen

print(friends[1:])          # select elements from index 1 to last index

print(friends[1:3])         # karen & jim

friends[1]= "Mike"          # 'Mike' instead of 'Karen'
print(friends[1])  
